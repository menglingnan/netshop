package entity;

public class Category{
	private int catId;
	private String name;
	private String cDesc;
	
	public int getCatId() {
		return catId;
	}
	public void setCatId(int catId) {
		this.catId = catId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCDesc() {
		return cDesc;
	}
	public void setCDesc(String desc) {
		this.cDesc = desc;
	}
	
}